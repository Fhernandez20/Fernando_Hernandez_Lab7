    private void jButton1ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButton1ActionPerformed
        String username = txt_nombre.getText();
        String password = new String(txt_password.getText());

        if (txt_nombre.getText().equals("") || txt_password.getText().equals("")) {
            JOptionPane.showMessageDialog(null, "llene todos los campos");

        } else {
            if (registro.agregarJugadores(username, password)) {

                JOptionPane.showMessageDialog(null, "cuenta creada exitosamente.");
            } else {
                JOptionPane.showMessageDialog(null, "Error al crear usuario.");
                
                txt_nombre.setText("");
                txt_password.setText("");
            }
            count++;
        }

        if (count == 1) {
            nom.setText("Jugador 2:");
            txt_nombre.setText("");
            txt_password.setText("");

        } else if (count >= 2) {
            HOME menu= new HOME(registro);
            menu.setVisible(true);
            this.dispose();
        }
    }//GEN-LAST:event_jButton1ActionPerformed


